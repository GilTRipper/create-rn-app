#!/usr/bin/env node
/**
 * Writes ios/.xcode.env.local with NODE_BINARY for Xcode build scripts.
 * Runs on project create (postinstall) so each machine uses its own Node path.
 */
const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const projectRoot = path.resolve(__dirname, "..");
const xcodeEnvLocalPath = path.join(projectRoot, "ios", ".xcode.env.local");

function resolveNodeBinary() {
  if (process.execPath && fs.existsSync(process.execPath)) {
    return process.execPath;
  }

  const shellProbes = [
    "command -v node",
    'export PATH="/opt/homebrew/bin:/usr/local/bin:$PATH"; command -v node',
    'source "$HOME/.nvm/nvm.sh" 2>/dev/null; command -v node',
    'eval "$("$HOME/.fnm/fnm" env)" 2>/dev/null; command -v node',
  ];

  for (const probe of shellProbes) {
    try {
      const resolved = execSync(`bash -lc '${probe}'`, {
        encoding: "utf8",
        stdio: ["ignore", "pipe", "ignore"],
      }).trim();

      if (resolved && fs.existsSync(resolved)) {
        return resolved;
      }
    } catch {
      // try next probe
    }
  }

  return null;
}

function main() {
  const nodeBinary = resolveNodeBinary();

  if (!nodeBinary) {
    console.warn("[setup-xcode-env] Could not find node. Create ios/.xcode.env.local manually.");
    process.exit(0);
  }

  const contents = `# Auto-generated by scripts/setup-xcode-env.js — local only (gitignored)
# Re-run: pnpm setup:ios-env
export NODE_BINARY="${nodeBinary}"
`;

  fs.mkdirSync(path.dirname(xcodeEnvLocalPath), { recursive: true });
  fs.writeFileSync(xcodeEnvLocalPath, contents, "utf8");
  console.log(`[setup-xcode-env] NODE_BINARY → ${nodeBinary}`);
}

main();
